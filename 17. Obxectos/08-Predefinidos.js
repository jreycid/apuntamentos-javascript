/*
    OBXECTOS PREDEFINIDOS

        Objeto console

            Utilízase para amosar mensaxes por consola

        Obxecto window

            Cando traballamos co navegador web, o obxecto principal é window,
            que ten innumerables propiedades e métodos.

            Algúns dos mais interesantes son os que nos proporcionan caixas de diálogo:

                alert
                confirm
                prompt

        Obxecto document

            A través deste obxecto accedemos ao DOM (Document Object Model),
            Deste xeito podemos acceder a calquera elemento do documento e modificar o seu contido.

            Métodos interesantes son:

                createElement
                remove
                appendChild
                getElementById
                getElementsByClassName
                querySelector
                getAttribute
                setAttribute

            Propiedades interesantes son:

                textContent
                innerHTML
                value ( utilizado para inputs )

        Enrarase mais en detalle no apartado adicado o DOM
 */
